void game(){
        xBall+= dirXBall;
        yBall+= dirYBall;

        while(!spriteMove(ball, xBall, yBall)){
            if(yBall == 3){
                yBall = 4;
                dirYBall = 1;
            }
            if(yBall == 26){
                yBall = 25;
                dirYBall = -1;
            }
            if(xBall == 3){
                xBall = 4;
                dirYBall = 0;
                dirXBall = 0;
                stop = !stop;
                scroll(0,30);

            }
            if(xBall == 37){
                xBall = 36;
                dirXBall = 0;
                dirYBall = 0;
                stop = !stop;
                scroll(0,30);
            }
        }
        
        if(dirXBall == -2){
            if(yBall > yPlayer1){
                yPlayer1++;
                while(!spriteMove(player1_3, 4, yPlayer1 + 1))
                    yPlayer1--;
                spriteMove(player1_2, 4, yPlayer1);
                spriteMove(player1_1, 4, yPlayer1 - 1);
            }       
            if(yBall < yPlayer1){
                yPlayer1--;
                while(!spriteMove(player1_1, 4, yPlayer1 - 1))
                    yPlayer1++;
                spriteMove(player1_2, 4, yPlayer1);
                spriteMove(player1_3, 4, yPlayer1 + 1);
            }
        }
        if(demo){
            if(dirXBall == 2){
                if(yBall > yPlayer2){
                    yPlayer2++;
                    while(!spriteMove(player2_3, 36, yPlayer2 + 1))
                        yPlayer2--;
                    spriteMove(player2_2, 36, yPlayer2);
                    spriteMove(player2_1, 36, yPlayer2 - 1);
                }       
                if(yBall < yPlayer2){
                    yPlayer2--;
                    while(!spriteMove(player2_1, 36, yPlayer2 - 1))
                        yPlayer2++;
                    spriteMove(player2_2, 36, yPlayer2);
                    spriteMove(player2_3, 36, yPlayer2 + 1);
                }
            }
        }
        if(xBall==5){
            if((yBall <= yPlayer1 + 1 && yBall >= yPlayer1 - 1))
                dirXBall = 2;
            if((yBall == yPlayer1 + 2) && (dirYBall < 0)){
                dirXBall = 2;
                dirYBall = 1;
            }
            if((yBall == yPlayer1 - 2) && (dirYBall > 0)){
                dirXBall = 2;
                dirYBall = -1;
            }
        }
        
        if(xBall==35){
            if((yBall <= yPlayer2 + 1 && yBall >= yPlayer2 - 1))
                dirXBall = -2;
            if((yBall == yPlayer2 + 2) && (dirYBall < 0)){
                dirXBall = -2;
                dirYBall = 1;
            }
            if((yBall == yPlayer2 - 2) && (dirYBall > 0)){
                dirXBall = -2;
                dirYBall = -1;
            }
        }
}

