void IO_listener(){
    if(!stop && !demo){
        if(BUTTON_0){
            yPlayer2--;
            while(!spriteMove(player2_1, 36, yPlayer2 - 1))
                yPlayer2++;
            spriteMove(player2_2, 36, yPlayer2);
            spriteMove(player2_3, 36, yPlayer2 + 1);
        }
        if(BUTTON_1){
            yPlayer2++;
            while(!spriteMove(player2_3, 36, yPlayer2 + 1))
                yPlayer2--;
            spriteMove(player2_2, 36, yPlayer2);
            spriteMove(player2_1, 36, yPlayer2 - 1);
        }
    }
    else{
        if(BUTTON_0){
            option = 42;
        }
        if(BUTTON_1){
            option = 45;
        }
        spriteMove(select, 16, option);
    }
}

void IO_interrupt(){
    if(!stop){
        scroll(0,30);
    }
    else{
        if(option == 42){
            reset();
        }
        if(option == 45){
            reset();
            demo = 1;
        }
        scroll(0,-30);
    }
    stop = !stop;
}